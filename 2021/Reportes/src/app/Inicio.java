    private void genReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genReportActionPerformed
        try {
            //Creamos la conexi칩n con la BD
            Conexion con = new Conexion();
            Connection conn = con.getConexion();

            //Creamos una variable de tipo JasperReport
            JasperReport reporte = null;

            //Creamos una variable String que contiene el path del archivo
            String path = "src/reportes/report1.jasper";

            //Agregar la variable que actuar치 de filtro
            //Para ello hacemos un mapeo de los par치metros con una variable de tipo Map
            Map parametro = new HashMap();
            //Se le manda el nombre del parametro que se usa en la consulta SQL como filtro WHERE
            //y el valor que tiene que tener este campo
            //parametro.put("id_estado", 15);

            int parametroDat = (int) jsIdEstado.getValue();
            parametro.put("id_estado", parametroDat);

            //Le damos un valor a la variable JasperReport
            reporte = (JasperReport) JRLoader.loadObjectFromFile(path);

            //Le damos contenido al informe (JQuery)
            JasperPrint jprint = JasperFillManager.fillReport(reporte, parametro, conn);

            //Creamos la vista del reporte
            JasperViewer view = new JasperViewer(jprint, false);

            //A침adimos que se pueda cerrar el informe
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);

            //Poner visible el reporte
            view.setVisible(true);


        } catch (JRException ex) {
            Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_genReportActionPerformed

